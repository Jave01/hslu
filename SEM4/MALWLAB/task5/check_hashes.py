import ctypes


def get_hash(s):
    num1 = ctypes.c_ulonglong(14695981039346656037)
    try:
        for num2 in s.encode("utf-8"):
            num1.value ^= num2
            num1.value *= 1099511628211
    except:
        pass
    return ctypes.c_ulonglong(num1.value ^ 6605813339339102567).value


